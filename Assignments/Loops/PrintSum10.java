/*
 Write a program to calculate the sum of first 10 natural number.
 */



// code







public class PrintSum10
{
	public static void main (String[] args) throws java.lang.Exception
	{
		
      int sum = 0;
      for(int i=1;i<=10;i++){
        sum +=i;
      }
      System.out.println(sum);
	}
}